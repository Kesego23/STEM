    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int userid =Integer.valueOf(txtid.getText());
        String password = txtpass.getText();

        try {
            rs=StemControl.loginCustomers(userid, password);
            if(rs.next()){
                JOptionPane.showMessageDialog(this, "user id and password matched and you are logged in as " + rs.getString("customernumber"));
                CustomerHome c = new CustomerHome();
                c.setVisible(true);
                this.setVisible(false);
            }else{
                JOptionPane.showMessageDialog(this, "user id and/or password are incorrect");
            }
        } catch (SQLException ex) {
            Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

