    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String username = txtusername.getText();
        String password = txtpass.getText();
        String usertype =  String.valueOf(jComboBox1.getSelectedItem());
        
        try {
            rs=StemControl.loginUsers(username, password, usertype);
            if(rs.next()){
                JOptionPane.showMessageDialog(this, "username and password matched and you are logged in as " + rs.getString("usertype"));
            
                if(jComboBox1.getSelectedIndex()==0){
                    AdminHome a = new AdminHome();
                    a.setVisible(true);
                    this.setVisible(false);
                    
                }else if(jComboBox1.getSelectedIndex()==1){
                    UserHome u = new UserHome();
                    u.setVisible(true);
                    this.setVisible(false);
                }else{
                    CustomerHome c = new CustomerHome();
                    c.setVisible(true);
                    this.setVisible(false);
                }
            }else{
                 JOptionPane.showMessageDialog(this, "username and/or password are incorrect");
            }
        } catch (SQLException ex) {
            Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

